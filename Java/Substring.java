public class Substring {
	public static void main(String[] args) {
	    String str= "Java Programming";
	    System.out.println("Given String is => "+str);
	    System.out.println("str.substring(7) is => "+str.substring(7));
	    System.out.println("::above method gives substring from 8th index to last excluding index number 8\n");
	    System.out.println("str.substring(2,8) is => "+str.substring(2,8));
	    System.out.println("::above method gives substring from 2nd index to 8th index including index 2 and excluding index 8");
	}
}